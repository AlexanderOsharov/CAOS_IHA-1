# Макросы для базового ввода-вывода через системные вызовы RARS

# Вывод строки
# %str — метка строки (адрес в .data)
.macro print_str(%str)
    li a7, 4            # Код системного вызова для печати строки
    la a0, %str         # a0 = адрес строки
    ecall               # Вызов ОС
.end_macro

# Вывод целого числа
# %reg — регистр с числом для вывода
.macro print_int(%reg)
    li a7, 1            # Код системного вызова для печати целого
    mv a0, %reg         # a0 = значение для вывода
    ecall               # Вызов ОС
.end_macro

# Вывод одного символа
# %reg — регистр с ASCII-кодом символа
.macro print_char(%reg)
    li a7, 11           # Код системного вызова для печати символа
    mv a0, %reg         # a0 = ASCII-код символа
    ecall               # Вызов ОС
.end_macro

# Завершение программы
.macro exit()
    li a7, 10           # Код системного вызова для выхода
    ecall               # Вызов ОС
.end_macro